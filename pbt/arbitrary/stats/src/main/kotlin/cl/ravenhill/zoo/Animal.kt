package cl.ravenhill.zoo

/**
 * Represents a generic animal type.
 *
 * This interface is used to define different kinds of animals as objects.
 */
interface Animal

/**
 * Represents a lion.
 *
 * A lion is a carnivorous animal typically found in savannas and grasslands.
 */
object Lion : Animal

/**
 * Represents a penguin.
 *
 * A penguin is a flightless bird that primarily inhabits cold regions and is known for its swimming ability.
 */
object Penguin : Animal

/**
 * Represents a turtle.
 *
 * A turtle is a reptile known for its protective shell and ability to live in both aquatic and terrestrial
 * environments.
 */
object Turtle : Animal
